public class operations {
    public static void main(String1[] args){
        /*
        A.O 

        - Add
        - Sub
        - Mul
        - Div

        -> Mod
        -> ++
        -> --
        
         */

        int num1, num2;
        num1 = 10;
        num2 = 20;

        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2); 


        /* 
        int num1 = 10;
        int num2 = 3;
        double num3 = 3;

        num1/num2 = 3
        num1/num3 = 3.3
        num3/num1 = 0.3
        */

        /*


         */

         System.out.println(5 % 10); // whenever leftside is smaller than right, answer would be the left

        
        int NUM = 10;
        /*
        INCREMENT

        PRE ++ -> ++num
        POST ++ -> num++ -> first prints value, then increments value
         
         */
        
        System.out.println(NUM);//10
        System.out.println(NUM++);//10
        System.out.println(NUM);//11
        System.out.println(--NUM);//10
        System.out.println(NUM);//10

    
    }
}
